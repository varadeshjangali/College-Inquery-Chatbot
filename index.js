document.addEventListener("click", (event) => {
    // Map button IDs to the respective staff details for all branches
    const staffDetailsMapping = {
        "staff-btn-computer": "computer_staff_detail",
        "staff-btn-electrical": "electrical_staff_detail",
        "staff-btn-civil": "civil_staff_detail",
        "staff-btn-electronics": "electronics_staff_detail",
        "staff-btn-information": "information_staff_detail",
        "staff-btn-mechanical": "mechanical_staff_detail",
    };

    const selectedStaff = staffDetailsMapping[event.target.id];
    if (selectedStaff) {
        // Clear the chat and show the staff details for the selected branch
        showMessage(data[selectedStaff].title);
        showOptions(data[selectedStaff].options);
    }
});

